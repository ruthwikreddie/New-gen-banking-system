    private void search()
    {
        tbtnExport.setEnabled(false);
        mainScroll.setHorizontalScrollBarPolicy(JScrollPane.HORIZONTAL_SCROLLBAR_NEVER);

        setCursor(ViewUtility.WAIT_CURSOR);
        ViewUtility.clearTable(mainTable);
        operations = operationRepository.findAllByAccountNumber(accountNumber);
        
        if (operations.isEmpty()) {
            MessageUtility.info(customerMainForm, "No results found!");
        } else {
            tbtnExport.setEnabled(true);
            mainScroll.setHorizontalScrollBarPolicy(JScrollPane.HORIZONTAL_SCROLLBAR_ALWAYS);
        }

        ViewUtility.addRowsToTable(convertToRows(operations), mainTable);
        lblSelectedLine.setText(0 + "/" + mainTable.getRowCount());
        
        setCursor(ViewUtility.DEFAULT_CURSOR);
    }
    
    private void defineCurrentBalance()
    {
        setCursor(ViewUtility.WAIT_CURSOR);
        valCurrentBalance.setText(operationRepository.currentBalance(accountNumber).orElse(0.0).toString());
        setCursor(ViewUtility.DEFAULT_CURSOR);
    }
     
    private List<String[]> convertToRows(List<Operation> operations)
    {
        return operations.stream().map(customer -> {
            String[] row = new String[4];
            row[0] = customer.getCreatedAt().format(DateTimeFormatter.ofPattern("MM/dd/yyyy"));
            row[1] = customer.getAmount() + "";
            row[2] = customer.getType().toString();
            row[3] = customer.getAccountNumber();
            
            return row;
        }).collect(Collectors.toList());
    }
    
